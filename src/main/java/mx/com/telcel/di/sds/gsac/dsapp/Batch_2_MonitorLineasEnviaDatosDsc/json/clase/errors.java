package mx.com.telcel.di.sds.gsac.dsapp.Batch_2_MonitorLineasEnviaDatosDsc.json.clase;

public class errors {

	public String description;
	public String severityLevel;
	public String actor;
	public String code;
	public String businessMeaning;

	public String getDescription() {
		return description;
	}

	public void setDescription(String description) {
		this.description = description;
	}

	public String getSeverityLevel() {
		return severityLevel;
	}

	public void setSeverityLevel(String severityLevel) {
		this.severityLevel = severityLevel;
	}

	public String getActor() {
		return actor;
	}

	public void setActor(String actor) {
		this.actor = actor;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getBusinessMeaning() {
		return businessMeaning;
	}

	public void setBusinessMeaning(String businessMeaning) {
		this.businessMeaning = businessMeaning;
	}

}
